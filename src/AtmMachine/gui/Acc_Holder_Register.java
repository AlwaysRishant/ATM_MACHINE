    private void btnregisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnregisterActionPerformed

        if (validateField()==-1) {
            JOptionPane.showMessageDialog(null, "plzz fill Name and Email", "WARNING!!",JOptionPane.WARNING_MESSAGE);
            return;
        }
        if(validateDeposit()==1){
            JOptionPane.showMessageDialog(null, "Fill the Deposit Field or Numeric number", "WARNING!!",JOptionPane.WARNING_MESSAGE);
            return;
        }
        arr=String.valueOf(pin());
        try{
            if (UserDAO.checkPin(arr)) 
            {
                email=lblemail.getText();
                UserPojo user=new UserPojo();
                user.setSifc(lblsifc.getText());
                user.setAcc_name(lblname.getText());
                user.setPin(String.valueOf(arr));
                user.setEmail(lblemail.getText());
                user.setIni_deposit(Double.parseDouble(lbldeposit1.getText()));
                CurrentUser.setSifc(lblsifc.getText());
                CurrentUser.setPin(String.valueOf(arr));
                CurrentUser.setDeposit(Double.parseDouble(lbldeposit1.getText()));
                int i=UserDAO.userInfo(user);
                if (i==1) {
                    sendEmail.sendMail(lblsifc.getText(),String.valueOf(arr),lblname.getText(), lblemail.getText());
                    JOptionPane.showMessageDialog(null, "Account Holder Record inserted Succesfully and Account pin and Details sent to your Mail", "SUCCES!!",JOptionPane.INFORMATION_MESSAGE);
                    new Home().setVisible(true);
                    this.dispose();
                    clearAll();
                }
                else{
                    JOptionPane.showMessageDialog(null, "problem in inserting the record in database", "Error!!",JOptionPane.ERROR_MESSAGE);
                }    
            }
            else{
                JOptionPane.showMessageDialog(null, "Due to Mistake of Bank The Generated PIN Already Used By One Of Customer of this !!\n\nPLZZ CREATE ACCOUNT AGAIN ", "PROBLEM OCCUR!!",JOptionPane.INFORMATION_MESSAGE);
                clearAll();
            }   
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null, "DB error","Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }   
    }//GEN-LAST:event_btnregisterActionPerformed

