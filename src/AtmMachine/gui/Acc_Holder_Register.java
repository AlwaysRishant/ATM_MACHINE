    public static char[] pin()
    {
        Random rd=new Random();
        String str="123456";
        char []arr=new char[6];
        for (int i = 0; i < str.length(); i++) {
            arr[i]=str.charAt(rd.nextInt(str.length()));
        }
        return arr;
    }
                    
